use crate::interpret::ast::ast::Label;

#[repr(usize)]
#[derive(Debug, Clone, PartialEq)]
pub enum OP {
    UNR = 0x00,
    NOP = 0x01,
    BLK(usize) = 0x02,
    LOOP(usize) = 0x03,
    CALL(Label) = 0x10,
    END = 0x0b,
    BR(usize) = 0x0C,
    BRIF(usize) = 0x0D,
    RET = 0x0F,
    DROP = 0x1a,
    LOCGET(usize) = 0x20,
    LOCSET(usize) = 0x21,
    LOCTEE(usize) = 0x22,
    GLOGET(Label) = 0x23,
    GLOSET(Label) = 0x24,
    I32LOAD = 0x28,
    I32STORE = 0x36,
    I32CONST(i32) = 0x41,
    I64CONST(i64) = 0x42,
    I32NE = 0x47,
    I32LTS = 0x48,
    I32ADD = 0x6a,
    I32MUL = 0x6c,
    I64ADD = 0x7c,
    I32REMU = 0x70,
    I32REMS = 0x6F,
    I32AND = 0x71,
    I32OR = 0x72,
    I32DIVU = 0x6E,
    I32DIVS = 0x6D,
    I32EQZ = 0x45,
    I32EQ = 0x46,
    I32GTS = 0x4A,
    I32LES = 0x4c,
    I32GES = 0x4e,
    I32XOR = 0x73,
    I32SUB = 0x6b,
}

